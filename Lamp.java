public class Lamp {

	// Boolean variable indicating if lamp is lit.
	private boolean isLit;
	
	// This method returns isLit and thus if the lamp is lit.
	public boolean getIsLit() {
		return isLit;
	}
	
	// This method sets isLit to the parameter.
	public void setIsLit(boolean isLit) {
		this.isLit = isLit;
	}
}
